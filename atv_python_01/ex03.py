# 3) Crie um programa que solicite dois valores inteiro a e b e imprima a soma de a+b, a subtração de a-b, a multiplicação de a*b e a divisão de a/b.

a = int(input('Valor de a: '))
b = int(input('Valor de b: '))
soma = a + b
subtracao = a - b 
multiplicacao = a * b
divisao = a / b

print(f'A soma de a + b é: {soma}') 
print(f'\nA subtração de a - b é: {subtracao}\n A multiplicação de a * b é: {multiplicacao}\n A divisão de a / b é: {divisao}')
